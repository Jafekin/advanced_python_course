#例-透视表与滑动窗口

import pandas as pd

df=pd.read_excel('航天进出口额.xlsx')  # 导入Excel文件，得到一个二维数据表

# 1、从指标中提取国家和地区
df['指标'] = df['指标'].str.split('_').str[-2]   #'_'切割后，从右往左第2个元素

# 2、重命名列名为“国家和地区”
df = df.rename(columns={'指标': '国家和地区'})
print("df：\n",df)
print()
df.to_excel("修改指标为国家和地区.xlsx")

# 3、创建pivot_table
pivot_df1 = pd.pivot_table(df, 
                          index=['国家和地区'], 
                          columns=['进出口'],
                          values=['2023-12', '2023-11', '2023-10'], 
                          aggfunc='sum', 
                          fill_value=0)

print("透视表1：\n",pivot_df1)
print()

pivot_df2 = pd.pivot_table(df, 
                          index=['国家和地区', '进出口'], #行索引有两个层次
                          values=['2023-12', '2023-11', '2023-10'], 
                          aggfunc='sum', 
                          fill_value=0)

print("透视表2：\n",pivot_df2)

#4、滑动窗口计算每个国家/地区最近3个月的进出口额的移动平均值
#（1）同时筛选行和列，提取24个月的贸易额
#【要进行算术运算，低版本Python必须只保留数值型数据，否则运行出错】
df_new=df.iloc[:,5:]  #通过df.iloc[ : , : ]选择序号，筛选所有行、第5列~最后一列的数据 【76行 x 24 列】   
print("筛选连续24列，df_new：\n",df_new)

#（2）滚动计算每个国家/地区每3个月的进出口额的移动平均值
rolling_avg = df_new.rolling(window=3, 
                         axis=1, 
                         min_periods=1, 
                         center=False, 
                         win_type=None
                         ).mean() 
print("每个国家/地区最近3个月的进口额的移动平均值：\n", rolling_avg)

